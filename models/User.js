const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    minlength: 3,
    maxlength: 20
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    match: /^\S+@\S+\.\S+$/
  },
  password: {
    type: String,
    required: true,
    minlength: 6
  },
  reviews: [{ type: mongoose.Schema.Types.ObjectId, ref: 'Review' }]
  // Other user attributes as per your requirements
});

const User = mongoose.model('User', userSchema);

module.exports = User;
